    private void borrarFilasTabla(DefaultTableModel tabla) {
        if (tabla != null) {
            int aux = tabla.getRowCount() - 1;
            for (int i = aux; i >= 0; i--) {
                tabla.removeRow(i);
            }
        }
    }

